#? === Что такое SQL? ===
# В этом файле мы объясним, что такое SQL и как он используется.

#* === 1. SQL — это язык запросов ===
# SQL расшифровывается как Structured Query Language (язык структурированных запросов).
# - Это стандартный язык для работы с реляционными базами данных.
# - Реляционные базы данных хранят данные в таблицах (например, таблица "студенты" или "курсы").
#
# SQL используется для:
# 1. Создания таблиц и структуры базы данных (DDL - Data Definition Language).
# 2. Вставки, обновления, удаления данных (DML - Data Manipulation Language).
# 3. Запросов для поиска и анализа данных (DQL - Data Query Language).

#* === 2. Преимущества SQL ===
# - Простота: SQL легко понять даже новичкам.
# - Мощность: SQL позволяет работать с большими наборами данных.
# - Популярность: SQL используется в большинстве баз данных (MySQL, PostgreSQL, SQLite, и т.д.).
# - Переносимость: Один и тот же SQL-код может работать с разными системами баз данных.

# Пример SQL-команд:
# - SELECT: получить данные.
# - INSERT: добавить данные.
# - UPDATE: обновить данные.
# - DELETE: удалить данные.


